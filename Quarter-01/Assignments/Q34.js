// Declare an array of strings named pizzaName and assign it various pizza names
var pizzaName = ["Margherita", "Pepperoni", "BBQ Chicken"];
// Start a for loop to iterate over each element in the pizzaName array
for (var a = 0; a < pizzaName.length; a++) {
    // Print a message stating that you like the current pizza
    console.log("I Like ".concat(pizzaName[a], " Pizza"));
}
// Print a message stating that you really love pizza
console.log("\nI Really Love The Pizza");
