// Declare a function named makeTshirt that takes two parameters: size (a string) and message (a string)
function makeTshirt(size, message) {
    // Print a message stating the size of the T-shirt
    console.log("The Size Of The T-shirt Is \"".concat(size, "\""));
    // Print a message stating the message printed on the T-shirt
    console.log("The Message Printed On The T-shirt Is \"".concat(message, "\"\n"));
}
// Call the makeTshirt function with the arguments "Small" and "I love Pakistan"
makeTshirt("Small", "I love Pakistan");
